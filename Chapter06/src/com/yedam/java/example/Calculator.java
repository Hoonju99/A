package com.yedam.java.example;

public class Calculator {

	
	int plus(int x, int y) {
		int result = x + y ;
		return result;
	}
	
	int min(int x, int y) {
		int result = x - y;
		return result;
	}
	
	
	double multi(double x, double y) {
		double result = x * y;
		return result;
	}
	
	
	double divide(double x, double y) {
		double result = x / y ;
		return result;
	}
	
	
	
	
	
	
	
	void println(String message) {
		System.out.println(message);
	}
	
	
}



